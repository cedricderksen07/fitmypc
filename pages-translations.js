function initializePageTranslation() {
    const currentLang = localStorage.getItem('language') || 'de';
    applyPageTranslations(currentLang);
}

function applyPageTranslations(lang) {
    const translations = pageTranslations[lang] || pageTranslations['de'];
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const text = translations[key];
        
        if (text) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (element.type === 'text' || element.type === 'email' || element.type === 'password' || element.type === 'number') {
                    element.placeholder = text;
                }
            } else if (element.tagName === 'LABEL' || element.tagName === 'BUTTON' || element.tagName === 'A') {
                element.textContent = text;
            } else {
                element.textContent = text;
            }
        }
    });
}

function changePageLanguage(lang) {
    localStorage.setItem('language', lang);
    applyPageTranslations(lang);
    
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = lang;
    }
}

document.addEventListener('DOMContentLoaded', () => {
    initializePageTranslation();
    
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = localStorage.getItem('language') || 'de';
    }
});
